def plus(a: int | float, b: int | float) -> int | float:
    return a + b

def minus(a: int | float, b: int | float) -> int | float:
    return a - b

def multiplication(a: int | float, b: int | float) -> int | float:
    return a * b

def division(a: int | float, b: int | float) -> int | float:
    return a / b